unsigned char pop_1st_bit(unsigned long long *bb)
{
  unsigned long long b = *bb ^ (*bb - 1);
  unsigned int fold = (unsigned) ((b & 0xffffffff) ^ (b >> 32));

  *bb &= (*bb - 1);

  return BitTable[(fold * 0x783a9b23) >> 26];
}
